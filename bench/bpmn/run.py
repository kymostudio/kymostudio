#!/usr/bin/env python3
"""BPMN bench — run everything and render the human-readable report.

Runs the correctness aggregator (`quality.py`) and the performance timing
(`perf.py`), writes `results/quality.json` + `results/perf.json`, and renders
`results/REPORT.md` — the combined scorecard you can read, link, or paste into a
PR / stakeholder note.

Run:  cd bench && uv run python bpmn/run.py [--reps N]
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path

import perf
import quality

HERE = Path(__file__).resolve().parent
ROOT = HERE.parents[1]
RESULTS = HERE / "results"


def _render_report(q: dict, p: dict) -> str:
    r, par, cov = q["render"], q["parity"], q["coverage"]
    env, corp, tim = p["environment"], p["corpus"], p["timing_ms"]
    return f"""# BPMN module — quality scorecard

> Generated by `bench/bpmn/run.py`. **Offline bench** — re-run with
> `cd bench && uv run python bpmn/run.py` to refresh. The *correctness* numbers
> are deterministic (they roll up committed test snapshots); the *performance*
> numbers are machine-dependent (host below) and informational, not a gate.

## 1. Correctness

| Metric | Value | Evidence |
|---|---|---|
| Render — full MIWG corpus | **{r['ok']}/{r['corpus_full']} ok ({r['pass_rate']:.1%})** | `{q['sources']['baseline_full']}` |
| Render — errors (crashes) | **{r['error']}** ({r['error_rate']:.1%}) | same |
| Render — empty-output (no-DI, skipped) | {r['empty_output']} | same |
| Render — PR gate subset | {r['gate_ok']}/{r['corpus_gate']} ok | `{q['sources']['baseline_gate']}` |
| Python↔JS parity — import | **{par['import_stems']} stems** | `{q['sources']['import_golden']}` |
| Python↔JS parity — export | {par['export_stems']} stems | `{q['sources']['export_golden']}` |
| Tracked divergences | **{par['divergences']}** ({par['parity_rate']:.0%} parity) | `{q['sources']['divergences']}` |
| BPMN element types mapped | {cov['elements_mapped']} (+ {cov['event_definitions']} event-definitions) | `{q['sources']['mapping']}` (BPMN-MAP-001) |

## 2. Performance

Host: `{env['platform']}` · Python {env['python']} · kymo {env['kymo_version']} ·
{env['cpu_count']} CPU · reps={env['reps']} · {env['timestamp']}

| Metric | Value |
|---|---|
| Corpus | {corp['files']} files ({corp['rendered']} rendered) — `{corp['dir']}` |
| Per-file (parse+render) — median | **{tim['per_file_median']} ms** |
| Per-file (parse+render) — p95 | {tim['per_file_p95']} ms |
| Full pass | {tim['total_pass']} ms (parse {tim['parse_total']} + render {tim['render_total']}) |
| Throughput | **{p['throughput_files_per_s']} files/s** |

## 3. How it is measured (offline scripts)

Three layers, all run by hand and snapshotted — none gate CI on its own:

- **Corpus regression** — `packages/python/tests/_bpmn_regress.py` renders every
  MIWG `.bpmn` and locks `(status, n_nodes, n_edges, SVG-hash)` against a
  committed baseline (`baseline.json` 120-file subset; `baseline_full.json` 840-file
  full corpus). A byte-level change is a regression.
- **Conformance** — `packages/python/tests/test_bpmn_conformance.py` locks that the
  Python and JS importers/exporters agree on the same canonical model
  (`conformance/golden/`); `known_divergences.json` lists any that don't.
- **Timing** — `bench/bpmn/perf.py` (this run) times parse + render over the
  vendored corpus.

`quality.py` only *reads* the first two snapshots — it never re-renders — so the
correctness table reflects the snapshots exactly as committed.

## 4. Element coverage

The importer/exporter maps {cov['elements_mapped']} BPMN 2.0 element types plus
{cov['event_definitions']} event-definition glyphs, against the normative
`docs/formats/bpmn/kymo-mapping.md` (BPMN-MAP-001), which mirrors the OMG BPMN
2.0.2 spec (ISO/IEC 19510:2013): events, tasks (incl. typed tasks), gateways,
sub-processes, pools/lanes/groups, data objects/stores, text annotations, and
sequence/message/association flows.

## 5. Honest limitations

- The {r['empty_output']} *empty-output* files carry no Diagram-Interchange
  geometry; the importer skips them by design (not an error — error count is
  **{r['error']}**).
- Performance numbers depend on the host above; treat them as a same-machine
  reference, not an absolute SLA.
- Correctness numbers are a snapshot of the committed baselines at run time;
  regenerate the baselines (with the suites' `KYMO_UPDATE_*` flags) before
  re-running this bench to reflect renderer changes.
"""


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--reps", type=int, default=perf.DEFAULT_REPS)
    ap.add_argument("--stamp", default=None)
    args = ap.parse_args()

    RESULTS.mkdir(parents=True, exist_ok=True)
    q = quality.collect()
    p = perf.measure(reps=args.reps, stamp=args.stamp)

    (RESULTS / "quality.json").write_text(
        json.dumps(q, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
    )
    (RESULTS / "perf.json").write_text(
        json.dumps(p, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
    )
    (RESULTS / "REPORT.md").write_text(_render_report(q, p), encoding="utf-8")

    print(f"render  : {q['render']['ok']}/{q['render']['corpus_full']} ok "
          f"({q['render']['pass_rate']:.1%}), {q['render']['error']} error")
    print(f"parity  : {q['parity']['divergences']} divergences "
          f"({q['parity']['parity_rate']:.0%})")
    print(f"perf    : median {p['timing_ms']['per_file_median']} ms/file, "
          f"{p['throughput_files_per_s']} files/s")
    for name in ("quality.json", "perf.json", "REPORT.md"):
        print(f"wrote   : {(RESULTS / name).relative_to(ROOT)}")


if __name__ == "__main__":
    main()
